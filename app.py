def parse_kinopoisk_reviews(movie_id, num_pages=3):
    """–ü–∞—Ä—Å–∏–Ω–≥ –æ—Ç–∑—ã–≤–æ–≤ —Å –ö–∏–Ω–æ–ø–æ–∏—Å–∫–∞"""
    reviews = []
    
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36',
        'Accept-Language': 'ru-RU,ru;q=0.9,en-US;q=0.8,en;q=0.7',
        'Referer': 'https://www.kinopoisk.ru/'
    }
    
    for page in range(1, num_pages + 1):
        try:
            url = f'https://www.kinopoisk.ru/film/{movie_id}/reviews/ord/date/status/all/perpage/200/page/{page}/'
            
            response = requests.get(url, headers=headers, timeout=10)
            soup = BeautifulSoup(response.text, 'html.parser')
            
            # –ò—â–µ–º –±–ª–æ–∫–∏ —Å –æ—Ç–∑—ã–≤–∞–º–∏
            review_blocks = soup.find_all('div', class_=['reviewItem', 'response'])
            
            for block in review_blocks:
                # –ò—â–µ–º —Ç–µ–∫—Å—Ç –æ—Ç–∑—ã–≤–∞
                review_text = ""
                
                # –ü—Ä–æ–±—É–µ–º —Ä–∞–∑–Ω—ã–µ —Å–µ–ª–µ–∫—Ç–æ—Ä—ã
                text_element = block.find('span', class_='_reachbanner_')
                if not text_element:
                    text_element = block.find('div', class_='brand_words')
                if not text_element:
                    text_element = block.find('p')
                
                if text_element:
                    review_text = text_element.get_text(strip=True)
                    
                    # –§–∏–ª—å—Ç—Ä—É–µ–º –º—É—Å–æ—Ä
                    if len(review_text) > 50 and not review_text.startswith('¬´'):
                        reviews.append(review_text)
                        print(f"üìù –ù–∞–π–¥–µ–Ω –æ—Ç–∑—ã–≤: {review_text[:80]}...")
            
            print(f"‚úÖ –°—Ç—Ä–∞–Ω–∏—Ü–∞ {page}: —Å–æ–±—Ä–∞–Ω–æ {len(review_blocks)} –æ—Ç–∑—ã–≤–æ–≤")
            time.sleep(random.uniform(2, 4))  # –ü–∞—É–∑–∞ –º–µ–∂–¥—É –∑–∞–ø—Ä–æ—Å–∞–º–∏
            
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ {page}: {e}")
            continue
    
    return reviews

